// Number Systems

#include <iostream>

int main() {
  int numberDecimal = 15; // Decimal
  int numberOctal = 017; // Octal
  int numberHexadecimal = 0x0f; // Hexadecimal
  int numberBinary = 0b00001111; // Binary

  std::cout << "numberDecimal: " << numberDecimal << std::endl;
  std::cout << "numberOctal: " << numberOctal << std::endl;
  std::cout << "numberHexadecimal: " << numberHexadecimal << std::endl;
  std::cout << "numberBinary: " << numberBinary << std::endl;

  return 0;
}